    private void cargarDatos() {

    DefaultComboBoxModel<Paciente> modeloPacientes = new DefaultComboBoxModel<>();

    for (Paciente p : sistema.getPacientes()) {

        modeloPacientes.addElement(p);

    }

    cmbPaciente.setModel(modeloPacientes);

    DefaultComboBoxModel<Doctor> modeloDoctores = new DefaultComboBoxModel<>();

    for (Doctor d : sistema.getDoctores()) {

        modeloDoctores.addElement(d);

    }

    cmbDoctor.setModel(modeloDoctores);

}
    

