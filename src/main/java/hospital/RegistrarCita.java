    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        
        try {

        int codigo = Integer.parseInt(txtCodigo.getText());

        Paciente paciente = (Paciente) cmbPaciente.getSelectedItem();

        Doctor doctor = (Doctor) cmbDoctor.getSelectedItem();

        String fecha = txtFecha.getText();

        Cita cita = new Cita(codigo, paciente, doctor, fecha);

        sistema.registrarCita(cita);

        JOptionPane.showMessageDialog(this, "Cita registrada correctamente.");

        txtCodigo.setText("");
        txtFecha.setText("");

        if (cmbPaciente.getItemCount() > 0) {
            cmbPaciente.setSelectedIndex(0);
        }

        if (cmbDoctor.getItemCount() > 0) {
            cmbDoctor.setSelectedIndex(0);
        }

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(this, "Ingrese un código válido.");

    }

        
    }//GEN-LAST:event_btnRegistrarActionPerformed

